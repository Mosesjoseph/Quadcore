public class LoadActivity extends FragmentActivity implements OnMapReadyCallback
{
    GoogleMap mMap;

    @Override
    public void onCreate(Bundle savedInstanceState)
    {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.load_activity);

       // setSupportActionBar(toolbar);

        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }
    @Override
    public void onMapReady(GoogleMap googleMap)
    {
        LatLng madisons = new LatLng(-25.765560, 28.305867);
        mMap.addMarker(new MarkerOptions().position(madisons).title("Blue Room").snippet("Event"));
    }
}
